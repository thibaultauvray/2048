#include "wong.h"

int	ft_movement(int ***m)
{
	int i;
	int j;

	i = 1;
	while (i < 3)
	{
		j = 0;
		while (j < 3)
		{
			if ((*m)[i][j] == 0)
				return (0);
			if ((*m)[i][j] == (*m)[i + 1][j] || (*m)[i][j] == (*m)[i][j + 1])
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
