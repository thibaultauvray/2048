#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	i;

	if (!dst && !src)
		return (NULL);
	i = 0;
	while (i < n)
	{
		((char *)dst)[i] = ((char *)src)[i];
		if ((unsigned char)(((char *)dst)[i]) == (unsigned char)c)
			return ((void *)(((char *)dst) + i + 1));
		i++;
	}
	return (NULL);
}
