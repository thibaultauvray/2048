#include "libft.h"

void	ft_strdel_ar(char ***a)
{
	int	i;

	if (*a)
	{
		i = 0;
		while ((*a)[i])
		{
			ft_strdel(&((*a)[i]));
			i++;
		}
		free(*a);
		a = NULL;
	}
}
